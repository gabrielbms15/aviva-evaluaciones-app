/// Retorna los miembros del staff para una sede y área dadas.
List<StaffMember> getStaffForArea(String sedeId, String areaId) {
  return allStaffMembers
      .where((s) => s.sedeId == sedeId && s.areaId == areaId)
      .toList();
}

/// Añade un nuevo miembro a una sede y área específicas.
void addStaffToArea(String sedeId, String areaId, String name, String role) {
  final currentLength = getStaffForArea(sedeId, areaId).length;
  final newId =
      '${sedeId}_${areaId}_s${currentLength + 1}_${DateTime.now().millisecondsSinceEpoch}';
  allStaffMembers.add(
    StaffMember(
      id: newId,
      name: name,
      sedeId: sedeId,
      areaId: areaId,
      role: role,
    ),
  );
}

/// Actualiza los datos (nombre y rol) de un miembro existente.
void updateStaff(String id, String newName, String newRole) {
  final index = allStaffMembers.indexWhere((s) => s.id == id);
  if (index != -1) {
    final oldStaff = allStaffMembers[index];
    allStaffMembers[index] = StaffMember(
      id: oldStaff.id,
      name: newName,
      sedeId: oldStaff.sedeId,
      areaId: oldStaff.areaId,
      role: newRole,
    );
  }
}
